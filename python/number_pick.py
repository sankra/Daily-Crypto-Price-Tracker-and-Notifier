import random

# Pick a random number between 1 and 26
random_number = random.randint(1, 26)

# Pick 5 unique numbers between 1 and 69
unique_numbers = random.sample(range(1, 70), 5)

print("Random number (1-26):", random_number)
print("5 unique numbers (1-69):", unique_numbers)
# Print the results
print("5 unique numbers (1-69):", unique_numbers)
print("Random number (1-26):", random_number)